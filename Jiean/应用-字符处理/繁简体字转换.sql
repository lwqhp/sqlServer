--生成码表
IF NOT OBJECT_ID('[dbo].[codetable]') IS NULL DROP table [dbo].[codetable]
create table codetable(gb nchar(1),big nchar(1))
select top 1298 id=identity(int,1,1) 
into #t 
from syscolumns a,syscolumns b

insert codetable(gb,big)
select substring(@gb,id,1),substring(@big,id,1)
from #t
where id<=len(@gb)

create unique clustered index PK_codetable on  codetable(gb,big)
drop table #t
go

