 --创建函数
 if object_id('dbo.fn_bg2big') is not null drop function dbo.fn_bg2big
 go
 create function dbo.fn_bg2big(
 @s nvarchar(max),	--要转换的字符串
 @toBig	bit			--转换标志,为,1 表示GB-->BIG,0 是BIG-->GB
 ) returns nvarchar(max)
 as
 begin 
 if @toBig = 1
	select @s=replace(@s,gb,big) from codetable where charindex(gb,@s)>0
 else
	select @s=replace(@s,big,gb) from codetable where charindex(big,@s)>0
 
 return @s
 end

