--物理数据模型PDM
/*
概念据模型用于数据库概念结构设计阶段，用E-R图描述实体以及实体之间的联系，主要包括实体，实体属性，联系，域，数据项等对象
物理数据模型则是在概念据模型基础上采用图形的方式描述数据的物理组织，主要包括表，表，主键，外键，索引，视图，存储过程和触发器

支持多种DBMS结构，真实反映数据的存储结构和关系
*/